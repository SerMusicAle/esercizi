
public class Pitagora {

	public static void main(String[] args) {
		double c1 = 45.3;
		double c2 = 67.2;
		
		
		
		double ipo = Math.sqrt(Math.pow(c1, 2) + Math.pow(c2,  2));

        System.out.println("The length of the hypotenuse is: " + ipo); // Output the result
    }

}

