# Nome: Frozenset - Creazione
# Codice:
fs = frozenset([1, 2, 3, 4])
# Output: frozenset({1, 2, 3, 4})
# Descrizione: Crea un frozenset con elementi 1, 2, 3, e 4.

# Nome: Frozenset - Controllo dell'appartenenza
# Codice:
print(1 in fs)
# Output: True
# Descrizione: Controlla se l'elemento 1 è presente nel frozenset.

# Nome: Frozenset - Unione
# Codice:
fs_union = fs.union([5, 6])
print(fs_union)
# Output: frozenset({1, 2, 3, 4, 5, 6})
# Descrizione: Unisce due frozenset.

# Nome: Frozenset - Intersezione
# Codice:
fs_intersection = fs.intersection([2, 3])
print(fs_intersection)
# Output: frozenset({2, 3})
# Descrizione: Trova l'intersezione di due frozenset.

# Nome: Frozenset - Differenza
# Codice:
fs_difference = fs.difference([3, 4])
print(fs_difference)
# Output: frozenset({1, 2})
# Descrizione: Trova la differenza tra due frozenset.

# Nome: Frozenset - Differenza simmetrica
# Codice:
fs_sym_difference = fs.symmetric_difference([3, 4, 5])
print(fs_sym_difference)
# Output: frozenset({1, 2, 5})
# Descrizione: Trova la differenza simmetrica tra due frozenset.

# Nome: Frozenset - Sottoset
# Codice:
print(fs.issubset([1, 2, 3, 4, 5, 6]))
# Output: True
# Descrizione: Verifica se il frozenset è un sottoinsieme di un altro set.

# Nome: Frozenset - Sovraset
# Codice:
print(fs.issuperset([1, 2]))
# Output: True
# Descrizione: Verifica se il frozenset è un sovrainsieme di un altro set.

# Nome: Frozenset - Copia
# Codice:
fs_copy = fs.copy()
print(fs_copy)
# Output: frozenset({1, 2, 3, 4})
# Descrizione: Crea una copia del frozenset.

